export function getPlacesByCategory(category: Category): Place[] {
  return places.filter((p) => p.category === category);
}

export function getPlaceBySlug(category: string, slug: string): Place | undefined {
  return places.find((p) => p.category === category && p.slug === slug);
}

export function getSimilarPlaces(place: Place, limit = 4): Place[] {
  return places
    .filter((p) => p.category === place.category && p.slug !== place.slug)
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit);
}

export function searchPlaces(query: string): Place[] {
  const q = query.toLowerCase();
  return places.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.city.toLowerCase().includes(q) ||
      p.tags.some((tag) => tag.toLowerCase().includes(q)) ||
      p.description.en.toLowerCase().includes(q) ||
      p.description.zh.includes(q) ||
      p.category.toLowerCase().includes(q)
  );
}

export function getPlacesByAge(age: AgeRange): Place[] {
  return places
    .filter((p) => p.ageRange.includes(age) || p.ageRange.includes("all"))
    .sort((a, b) => b.rating - a.rating);
}

export function getIndoorPlaces(): Place[] {
  return places
    .filter((p) => p.indoorOutdoor === "indoor" || p.indoorOutdoor === "both")
    .sort((a, b) => b.rating - a.rating);
}
